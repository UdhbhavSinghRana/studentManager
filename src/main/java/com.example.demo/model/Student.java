package com.example.demo.model;

public class Student {
    public String id;

    public String name;

    public int age;

    public String Adhar;

    public String Uni;

    public Student (String id, String name, int age, String adhar, String uni) {
        this.id = id;
        this.age = age;
        this.name = name;
        this.Adhar = adhar;
        this.Uni = uni;
    }
}
